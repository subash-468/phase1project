
public class typecasting {
    public static void main(String[] args) {
        int numInt = 400;
        long numLong = numInt;
  System.out.println("Implicit casting: " + numLong);
        double numDouble = 14.8;
        int numIntExplicit = (int) numDouble;
        System.out.println("Explicit casting: " + numIntExplicit);
    }
}

	


